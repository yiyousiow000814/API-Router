import { useProviderCrudActions } from './providerActions/useProviderCrudActions'
import { useProviderKeyActions } from './providerActions/useProviderKeyActions'
import { useProviderUsageActions } from './providerActions/useProviderUsageActions'
import type { UseProviderActionsParams, UseProviderActionsResult } from './providerActions/types'

export function useProviderActions({
  config,
  status,
  isDevPreview,
  setConfig,
  keyModal,
  providerBaseUrlModal,
  providerEmailModal,
  usageBaseModal,
  usageAuthModal,
  newProviderName,
  newProviderBaseUrl,
  newProviderKey,
  setKeyModal,
  setProviderBaseUrlModal,
  setProviderEmailModal,
  setUsageBaseModal,
  setUsageAuthModal,
  setNewProviderName,
  setNewProviderBaseUrl,
  setNewProviderKey,
  setRefreshingProviders,
  refreshStatus,
  refreshConfig,
  flashToast,
}: UseProviderActionsParams): UseProviderActionsResult {
  const {
    saveProvider,
    saveProviderBaseUrl,
    setProviderGroup,
    setProvidersGroup,
    setProviderDisabled,
    deleteProvider,
    openProviderBaseUrlModal,
    addProvider,
  } = useProviderCrudActions({
    config,
    isDevPreview,
    setConfig,
    providerBaseUrlModal,
    newProviderName,
    newProviderBaseUrl,
    newProviderKey,
    setProviderBaseUrlModal,
    setNewProviderName,
    setNewProviderBaseUrl,
    setNewProviderKey,
    refreshStatus,
    refreshConfig,
    flashToast,
  })

  const { saveKey, clearKey, openKeyModal } = useProviderKeyActions({
    keyModal,
    isDevPreview,
    setKeyModal,
    refreshStatus,
    refreshConfig,
    flashToast,
  })

  const {
    refreshQuota,
    refreshQuotaAll,
    saveUsageBaseUrl,
    saveUsageAuth,
    clearUsageAuth,
    saveProviderEmail,
    clearProviderEmail,
    setUsageBaseUrl,
    clearUsageBaseUrl,
    setProviderQuotaHardCap,
    openUsageBaseModal,
    openUsageAuthModal,
    openPackycodeLogin,
    openProviderEmailModal,
  } = useProviderUsageActions({
    config,
    status,
    setConfig,
    isDevPreview,
    providerEmailModal,
    usageBaseModal,
    usageAuthModal,
    setProviderEmailModal,
    setUsageBaseModal,
    setUsageAuthModal,
    setRefreshingProviders,
    refreshStatus,
    refreshConfig,
    flashToast,
  })

  return {
    saveProvider,
    setProviderGroup,
    setProvidersGroup,
    setProviderDisabled,
    deleteProvider,
    saveKey,
    clearKey,
    refreshQuota,
    refreshQuotaAll,
    saveUsageBaseUrl,
    saveUsageAuth,
    clearUsageAuth,
    saveProviderEmail,
    clearProviderEmail,
    saveProviderBaseUrl,
    setUsageBaseUrl,
    clearUsageBaseUrl,
    setProviderQuotaHardCap,
    openKeyModal,
    openProviderBaseUrlModal,
    openUsageBaseModal,
    openUsageAuthModal,
    openPackycodeLogin,
    openProviderEmailModal,
    addProvider,
  }
}
